  public function getRegionSEO($region, $page)
  {
    if (isset($this->regions[$region][$page])) {
      return $this->regions[$region][$page];
    } else {
      // Fallback zu Wien, wenn die Region oder Seite nicht existiert
      return isset($this->regions['Wien'][$page]) ? $this->regions['Wien'][$page] : null;
    }
  }

  // Beispielmethode zum Hinzufügen einer neuen Seite
  public function addPage($region, $pageKey, $pageData)
  {
    if (!isset($this->regions[$region])) {
      $this->regions[$region] = [];
    }
    $this->regions[$region][$pageKey] = $pageData;
  }
